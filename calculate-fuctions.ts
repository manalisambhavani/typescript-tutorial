export const sum = (value1: number, value2: number): number => value1 + value2;
export const sub = (value1: number, value2: number): number => value1 - value2;
export const mul = (value1: number, value2: number): number => value1 * value2;
export const div = (value1: number, value2: number): number => value1 / value2;
